function currencyConverter() {
    
  
    
    
     var e1 = document.getElementById("coin_to");
    var coin_to = e1.options[e1.selectedIndex].value;
    
     var e2 = document.getElementById("coin_from");
    var coin_from = e2.options[e2.selectedIndex].value;
    
     var amt_coin_from = document.getElementById("amt_coin_from");
     
     amt_coin_from = amt_coin_from.value;
    
    var amt_coin_to = (Number(amt_coin_from)* Number(coin_from))/Number(coin_to);
        
    document.getElementById("p").innerHTML = amt_coin_from +" "+e2.options[e2.selectedIndex].text+" ≈ "+amt_coin_to.toFixed(4)+" "+e1.options[e1.selectedIndex].text+"";
    document.getElementById("amt_coin_to").value = amt_coin_to.toFixed(4);
}
